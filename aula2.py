# aspas simples
print('Hello, world')

# aspas duplas
print("Hello, world")

# escape = barra ivertida
print("Hello, \"world\"")
# r
print(r"Hello, \"world\"")

# Como colocar aspas dentro de uma string?
print("Hello, 'world'")
print('Hello, "world"')
# basta inverter a aspas
# ou seja, aspas simples dentro de duplas e duplas dentro de simples.